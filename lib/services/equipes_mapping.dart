  /// Résout un code Excel (ex: "a1") pour une catégorie donnée
  /// Retourne {'id': 'R15M9', 'name': 'XV Purpanais'} ou null
  static Map<String, String>? resolve(String categorie, String codeExcel) {
    final code = codeExcel.toLowerCase().trim();
    switch (categorie) {
      case 'R15M': return r15m[code];
      case 'R7M':  return r7m[code];
      case 'R7F':  return r7f[code];
      default: return null;
    }
  }

  /// Retourne le mapping complet pour une catégorie sous forme de String
  /// pour l'injecter dans le prompt Claude
  static String toPromptString(String categorie) {
    final Map<String, Map<String, String>> map;
    switch (categorie) {
      case 'R15M': map = r15m; break;
      case 'R7M':  map = r7m;  break;
      case 'R7F':  map = r7f;  break;
      default: return '';
    }
    final lines = map.entries.map((e) =>
    '  ${e.key} → id="${e.value['id']}" name="${e.value['name']}"'
    ).join('\n');
    return '$categorie :\n$lines';
  }

  /// Mapping complet toutes catégories pour le prompt
  static String get fullPromptMapping =>
